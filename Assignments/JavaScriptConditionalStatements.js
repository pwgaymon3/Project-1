let num = 3;

if (num > 10) {
  console.log("The value of num is greater than 10");
} else {
  console.log("The value of num is less than or equal to 10");
}

switch(num) {
  case 1:
    console.log("The value of num is 1")
    break

  case 2:
    console.log("The value of num is 2")
    break

  case 3:
    console.log("The value of num is 3")
    break

  default:
    console.log("The value of num is not 1, 2, or 3")
}